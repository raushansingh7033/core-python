# Used to check if a value exists inside a sequence (like string, list, tuple, etc.).

text = "apple"
print("'a' in text:", 'a' in text)       # True (because 'a' is in "apple")
print("'b' not in text:", 'b' not in text)  # True (because 'b' is not in "apple")

numbers = [1, 2, 3, 4, 5]
print("3 in numbers:", 3 in numbers)     # True
print("10 not in numbers:", 10 not in numbers) # True
